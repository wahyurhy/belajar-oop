public class Kalkulator {
    // Polymorphism Overloading
    public int tambah(int angka1, int angka2) {
        return angka1 + angka2;
    }
    
    public int tambah(int angka1, int angka2, int angka3) {
        return angka1 + angka2 + angka3;
    }
}
